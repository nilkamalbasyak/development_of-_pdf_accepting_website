      <h1 class="text-center"style="color:blue">Scope</h1>

      <p class="">
        Many computational intelligence and learning methods, such as expert systems, neural networks, genetic algorithms and so on have found success
        in a variety of control and automation fields. Intelligent Control, which differs from conventional approaches is based on methodologies
        derived from Artificial Intelligence[AI] and soft computing techniques is capable of dealing with problems in diverse computational and
        communication domains and, more recently, those related to renewable energy and mobile autonomous systems, where conventional methods were
        unsuccessful. Nevertheless, new technologies such as artificial intelligence, edge computing, and the Internet of Things have led to an
        unprecedented development of control and intelligent systems.
      </p>
      <p class="">
        The theme of GIUAC 2022 is “Intelligent Computational Methods, Automation and Emerging trends in renewable energy". This International
        Conference on Automation, Computing and Renewable Systems [GIUAC 2022] is devoted to intelligent systems that improve the control of
        nonlinear systems and its applications and also the novel contributions in all aspects of automation and renewable systems. This conference
        will provide a knowledge-building and experience-sharing platform for professionals from the industry, academia and research organizations.
      </p>

